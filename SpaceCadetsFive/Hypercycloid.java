
public class Hypercycloid {

	double radius1;
	double radius2;
	double offset;
	int xPos;
	int yPos;
	
	public Hypercycloid(int xPos, int yPos, double radius1, double radius2, double offset) {
		this.xPos = xPos;
		this.yPos = yPos;
		this.radius1 = radius1;
		this.radius2 = radius2;
		this.offset = offset;
	}
}
